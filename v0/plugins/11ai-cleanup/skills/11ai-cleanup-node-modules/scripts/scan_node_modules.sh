#!/usr/bin/env bash
# scan_node_modules.sh [root] — find top-level node_modules directories under
# root (default: $HOME) and report the facts needed to judge abandonment.
#
# Output: tab-separated, one line per node_modules, sorted biggest first:
#   SIZE   AGE_D   LAST_COMMIT   FLAGS   PROJECT
# followed by a TOTALS footer: overall count and size, plus per-flag
# subtotals (stale = reclaimable, big, active) so reports can quote
# exact "disk space reclaimable" numbers.
#
#   SIZE         human-readable size of the node_modules directory
#   AGE_D        days since the parent project was last touched (package.json mtime)
#   LAST_COMMIT  relative date of the project's last git commit, or "-" if not a git repo
#   PROJECT      the project directory that owns this node_modules
#
# FLAGS (comma-joined, "-" if none):
#   stale   project untouched for over 60 days — likely abandoned
#   big     node_modules over 500 MB — high-value cleanup
#   active  project touched in the last 7 days — leave alone
#
# Strictly read-only. Works with stock macOS bash 3.2 and on Linux.
set -euo pipefail

ROOT="${1:-$HOME}"
now="$(date +%s)"

mtime_of() { stat -f %m "$1" 2>/dev/null || stat -c %Y "$1" 2>/dev/null || echo "$now"; }

# -prune on node_modules keeps only the outermost ones (nested copies go with
# their parent). Also prune slow/system trees we never want to walk into.
results="$(find "$ROOT" \
  \( -name Library -o -name .Trash -o -name .git -o -name .cache \) -prune -o \
  -type d -name node_modules -prune -print 2>/dev/null | while read -r dir; do
  proj="$(dirname "$dir")"

  size_kb="$(du -sk "$dir" 2>/dev/null | awk '{print $1}')"
  size_h="$(du -sh "$dir" 2>/dev/null | awk '{print $1}')"
  [ -z "${size_kb:-}" ] && continue

  ref="$proj/package.json"
  [ -f "$ref" ] || ref="$proj"
  age_d="$(( (now - $(mtime_of "$ref")) / 86400 ))"

  last_commit="-"
  if [ -d "$proj/.git" ]; then
    last_commit="$(git -C "$proj" log -1 --format=%cr 2>/dev/null || echo '-')"
  fi

  flags=""
  [ "$age_d" -gt 60 ] && flags="stale"
  [ "$size_kb" -gt 512000 ] && flags="${flags:+$flags,}big"
  [ "$age_d" -lt 7 ] && flags="${flags:+$flags,}active"

  printf '%s\t%s\t%s\t%s\t%s\t%s\n' "$size_kb" "$size_h" "$age_d" "$last_commit" "${flags:--}" "$proj"
done)"

if [ -z "$results" ]; then
  echo "No node_modules directories found under $ROOT."
  exit 0
fi

printf 'SIZE\tAGE_D\tLAST_COMMIT\tFLAGS\tPROJECT\n'
echo "$results" | sort -rn -k1,1 | cut -f2-
echo ""
echo "$results" | awk -F'\t' '
  function human(kb) {
    if (kb >= 1048576) return sprintf("%.1fG", kb/1048576)
    if (kb >= 1024)    return sprintf("%.0fM", kb/1024)
    return kb "K"
  }
  { n++; total += $1
    if ($5 ~ /stale/)  { ns++; ts += $1 }
    if ($5 ~ /big/)    { nb++; tb += $1 }
    if ($5 ~ /active/) { na++; ta += $1 } }
  END {
    printf "TOTALS: %d node_modules directories, %s on disk\n", n, human(total)
    if (ns) printf "  stale (reclaim candidates): %d, %s reclaimable\n", ns, human(ts)
    if (nb) printf "  big (>500MB): %d, %s\n", nb, human(tb)
    if (na) printf "  active (leave alone): %d, %s\n", na, human(ta)
  }'
